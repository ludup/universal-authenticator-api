package com.sshtools.universal;

public class RequestParameter {

	String name;
	String value;
	
	public RequestParameter(String name, String value) {
		super();
		this.name = name;
		this.value = value;
	}
	
	public String getName() {
		return name;
	}
	public String getValue() {
		return value;
	}
	
	
}
